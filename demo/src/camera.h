#ifndef _CAMERA_H_
#define _CAMERA_H_

#include <linalgb.h>

struct camera {
    vec3 pos;
    vec3 front;
    vec3 up;
    float yaw;
    float pitch;
    float move_speed;
    float sensitivity;
    float pitch_lim;
    mat4 view_mat;
    mat4 prev_view_mat;
};

enum camera_move_dir {
    cmd_forward  = 1 << 0,
    cmd_left     = 1 << 1,
    cmd_backward = 1 << 2,
    cmd_right    = 1 << 3
};

void camera_defaults(struct camera* cam);
void camera_move(struct camera* cam, int move_directions);
void camera_look(struct camera* cam, float offx, float offy);
void camera_update(struct camera* cam);
mat4 camera_interpolated_view(struct camera* cam, float interpolaton);

#endif /* ! _CAMERA_H_ */
