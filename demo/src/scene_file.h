#ifndef _SCENE_FILE_H_
#define _SCENE_FILE_H_

#include <stdlib.h>

struct scene {
    /* Models */
    struct scene_model {
        const char* ref;
        const char* path;
    }* models;
    size_t num_models;
    /* Textures */
    struct scene_texture {
        const char* ref;
        const char* path;
    }* textures;
    size_t num_textures;
    /* Materials */
    struct scene_material {
        const char* ref;
        const char* albedo_tex_ref;
    }* materials;
    size_t num_materials;
    /* Objects */
    struct scene_object {
        const char* ref;
        const char* name;
        struct {
            float translation[3];
            float rotation[4];
            float scaling[3];
        } transform;
        const char* mdl_ref;
        const char** mat_refs;
        size_t num_mat_refs;
        const char* parent_ref;
    }* objects;
    size_t num_objects;
};

struct scene* scene_from_file(const char* filepath);
void scene_destroy(struct scene* sc);

#endif /* ! _SCENE_FILE_H_ */
